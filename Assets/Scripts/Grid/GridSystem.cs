	void Awake(){
		SimpleGrid = Grid;
		for (int x = 0; x < SimpleGrid.GetLength(0); x++) {
			for (int y = 0; y < SimpleGrid.GetLength(1); y++) {
				if (SimpleGrid [x, y] == 2) {
					SimpleGrid[x,y] = 0;
				}
				else if (SimpleGrid [x, y] == 3) {
					int chance = Random.Range(0, 100);
					if(chance < wallSpawnChance) {
						SimpleGrid[x,y] = 1;
					}
					else {
						SimpleGrid[x,y] = 0;
					}
				}
			}
		}
		GetComponent<GridVisualiser>().Visualize();
		GameObject.Find ("A*").GetComponent<Grid>().StartGrid();
	}
